    public DlgOrders(java.awt.Frame parent, boolean modal, int month, int year) {
        super(parent, modal);
        initComponents();
        txtMonth.setText(String.valueOf(month));
        txtYear.setText(String.valueOf(year));
        LoadOrders();
        if (tblOrders.getRowCount() > 0) {
            if (tblOrders.getValueAt(0, 0) == null) {
                return;
            }
            // Load order
            Orders order = OrdersRepository.selectByID(Integer.parseInt(((JLabel) tblOrders.getValueAt(0, 0)).getName().trim()));
            // Load is discount
            txtDiscount.setText(String.valueOf(order.getDiscount()));
            txtOrderDate.setText(DateFormat.getInstance().format(order.getOrderdate()).split(" ")[0]);

            // Load list of order details
            LoadOrderDetails(order.getId());
        }
    }

    public void LoadOrders() {
        ArrayList<Orders> lstItem = OrdersRepository.selectByMonthAndYear(Integer.parseInt(txtMonth.getText()), Integer.parseInt(txtYear.getText()));
        MyTable.TableOders.loadDataIntoTableBook(tblOrders, lstItem);
    }

    public void LoadOrderDetails(int orderid) {
        ArrayList<Orderdetails> lstItem = OrderDetailsRepository.selectByOrder(orderid);
        MyTable.TableOrderDetails.loadDataIntoTableBook(tblOrderDetails, lstItem);
        // Calculate total balance
        long total = 0;
        for(int i=0; i<lstItem.size(); i++){
            total += lstItem.get(i).getQuantity() * lstItem.get(i).getPrice();
        }
        txtTotal.setText(String.valueOf(total));
    }

