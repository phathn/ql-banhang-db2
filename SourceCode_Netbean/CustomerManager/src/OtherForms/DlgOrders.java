    private void tblOrdersMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOrdersMousePressed
        // TODO add your handling code here:
        int modifiers = evt.getModifiers();
        if ((modifiers & InputEvent.BUTTON1_MASK) == InputEvent.BUTTON1_MASK) {
            if (this.tblOrders.getSelectedRow() == -1) {
                return;
            }
            if (tblOrders.getValueAt(this.tblOrders.getSelectedRow(), 0) == null) {
                return;
            }
            if (evt.getClickCount() == 1) {
                String id = ((JLabel) tblOrders.getValueAt(this.tblOrders.getSelectedRow(), 0)).getName().trim();
                LoadOrderDetails(Integer.parseInt(id));
            }
        }
    }//GEN-LAST:event_tblOrdersMousePressed

